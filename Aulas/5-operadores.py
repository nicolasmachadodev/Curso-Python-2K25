# Operadores matemáticos
a = 10
b = 3

print("Soma:", a + b)        # Soma → 13
print("Subtração:", a - b)   # Subtração → 7
print("Multiplicação:", a * b) # Multiplicação → 30
print("Divisão:", a / b)     # Divisão → 3.333...
print("Divisão inteira:", a // b) # Divisão inteira → 3
print("Resto da divisão:", a % b) # Módulo → 1
print("Exponenciação:", a ** b)   # Potência → 1000

# Operadores de comparação
print("a é igual a b?", a == b)     # False
print("a é diferente de b?", a != b) # True
print("a é maior que b?", a > b)     # True
print("a é menor que b?", a < b)     # False
print("a é maior ou igual a b?", a >= b) # True
print("a é menor ou igual a b?", a <= b) # False

# Operadores de atribuição
x = 5
print("\nValor inicial de x:", x)

x += 2  # x = x + 2
print("x após x += 2:", x)

x *= 3  # x = x * 3
print("x após x *= 3:", x)

x -= 1  # x = x - 1
print("x após x -= 1:", x)

x /= 2  # x = x / 2
print("x após x /= 2:", x)
