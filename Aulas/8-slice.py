texto = "programação"

print(texto[0:4])   # "prog" (do índice 0 até o 3)
print(texto[4:7])   # "ram" (índices 4, 5 e 6)
print(texto[:5])    # "progr" (do começo até o índice 4)
print(texto[5:])    # "amação" (do índice 5 até o final)
print(texto[-3:])   # "ção" (últimos 3 caracteres)
print(texto[::2])   # "pormço" (pulando de 2 em 2)
print(texto[::-1])  # "oãçamacargorp" (string invertida)

print(texto[3:-2])
