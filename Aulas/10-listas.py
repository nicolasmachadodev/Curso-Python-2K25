gamesList = ["Red Dead Redemption 2", "Fifa 22", "GTA V", "Limbo", "Hollow Knight"]

# Exemplos de fatiamento:

# Pega os três primeiros jogos
print(gamesList[0:3])      

# Pega do terceiro jogo até o final
print(gamesList[2:])      

# Pega os dois primeiros jogos
print(gamesList[:2])

# Pega os dois últimos jogos
print(gamesList[-2:])      

# Pega todos os jogos, pulando de 2 em 2
print(gamesList[::2])      

# Inverte a lista
print(gamesList[::-1])     